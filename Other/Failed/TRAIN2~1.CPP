#include <stdio.h>
#include <string.h>
int main()
{
	long long a[200][101][5] = {0};//定义数组，a[i][j][k]表示\
	第i+1次进出栈操作后，栈中共有j个数，因为组合数目过大，所以加一维，用于进位 
	int n,i,j,k;
	while(scanf("%d",&n)!=EOF)
	{
		a[0][1][0] = 1;//初始化数组，第一次栈操作必须是进栈，第一次hou，站内有一个数。
		for(i=1;i<2*n;i++){
			for(j=0;j<=n;j++){
				for(k=0;k<=4;k++){
					if(j!=0){
					a[i][j-1][k] += a[i-1][j][k];//一次栈操作后，栈内的数要么加一，要么减1							
					if(a[i][j-1][k]>100000000000000000){
								a[i][j-1][k+1] += a[i][j-1][k]/100000000000000000;//十七个零 
								a[i][j-1][k] = a[i][j-1][k]%100000000000000000;
							}
						}
					if(j!=n){
						a[i][j+1][k] += a[i-1][j][k];
						if(a[i][j+1][k]>100000000000000000){
							a[i][j+1][k+1] += a[i][j+1][k]/100000000000000000;
							a[i][j+1][k] = a[i][j+1][k]%100000000000000000;
						}
					}
				}
			}
		}
	
		if(a[2*n-1][0][4]==0)
			if(a[2*n-1][0][3]==0)
				if(a[2*n-1][0][2]==0)
					if(a[2*n-1][0][1]==0)
						printf("%lld\n",a[2*n-1][0][0]);
					else
						printf("%lld%017lld\n",a[2*n-1][0][1],a[2*n-1][0][0]);		
				else
					printf("%lld%017lld%017lld\n",a[2*n-1][0][2],a[2*n-1][0][1],a[2*n-1][0][0]);
			else
				printf("%lld%017lld%017lld%017lld\n",a[2*n-1][0][3],a[2*n-1][0][2],a[2*n-1][0][1],a[2*n-1][0][0]);	
		else
			printf("%lld%017lld%017lld%017lld%017lld\n",a[2*n-1][0][4],a[2*n-1][0][3],a[2*n-1][0][2],a[2*n-1][0][1],a[2*n-1][0][0]);
		
		memset(a,0,sizeof(a));
	}
	return 0;
}
